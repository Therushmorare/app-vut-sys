"use client";

import { useState } from "react";
import axios from "axios";
import { GraduationCap, Mail } from "lucide-react";
import { COLORS } from "../../constants/colors";
import { useRouter } from "next/navigation";

export default function Forgot({ onSwitchToRegister }) {
  const [formData, setFormData] = useState({ email: "" });
  const [errors, setErrors] = useState({});
  const [loading, setLoading] = useState(false);
  const [success, setSuccess] = useState("");
  const [error, setError] = useState("");

  const router = useRouter();

  const handleChange = (field, value) => {
    setFormData((prev) => ({ ...prev, [field]: value }));
    setErrors({});
    setError("");
    setSuccess("");
  };

  const handleSubmit = async (e) => {
    e.preventDefault();

    if (!formData.email.trim()) {
      setErrors({ email: "Email is required" });
      return;
    }

    setLoading(true);

    try {
      const response = await axios.post(
        "https://d17qozs0vubb7e.cloudfront.net/api/students/forgot-password",
        { email: formData.email.trim() },
        { headers: { "Content-Type": "application/json" } }
      );

      setSuccess(response.data.message || "Password reset email sent!");
      console.log("Forgot Password response:", response.data);

    } catch (err) {
      console.error("Forgot Password error:", err);
      setError(err.response?.data?.message || "Something went wrong.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div
      className="min-h-screen flex items-center justify-center p-4"
      style={{ backgroundColor: COLORS.bgLight }}
    >
      <div
        className="w-full max-w-md rounded-lg shadow-lg p-8"
        style={{ backgroundColor: COLORS.bgWhite }}
      >
        {/* Header */}
        <div className="text-center mb-8">
          <div className="flex justify-center mb-4">
            <GraduationCap className="w-16 h-16" style={{ color: COLORS.primary }} />
          </div>
          <h1 className="text-3xl font-bold mb-2" style={{ color: COLORS.primary }}>
            Forgot Password
          </h1>
          <p className="text-gray-600">Enter your email to reset your password</p>
        </div>

        {/* Error / Success */}
        {error && <p className="text-sm text-center mb-4" style={{ color: COLORS.danger }}>{error}</p>}
        {success && <p className="text-sm text-center mb-4" style={{ color: COLORS.success }}>{success}</p>}

        {/* Form */}
        <form onSubmit={handleSubmit} className="space-y-4">
          <div>
            <label className="block text-sm font-medium mb-2 text-gray-700">
              Email
            </label>
            <div className="relative">
              <Mail className="absolute left-3 top-1/2 transform -translate-y-1/2 w-5 h-5 text-gray-400" />
              <input
                type="email"
                value={formData.email}
                onChange={(e) => handleChange("email", e.target.value)}
                placeholder="your.email@university.ac.za"
                className="w-full pl-10 pr-4 py-2 border rounded-lg focus:outline-none focus:ring-2"
                style={{
                  borderColor: errors.email ? COLORS.danger : COLORS.border,
                }}
              />
            </div>
          </div>

          <button
            type="submit"
            disabled={loading}
            className="w-full py-3 rounded-lg text-white font-semibold hover:opacity-90 transition-opacity"
            style={{ backgroundColor: COLORS.primary }}
          >
            {loading ? "Sending..." : "Send Reset Link"}
          </button>
        </form>

        <div className="mt-6 text-center">
          <p className="text-gray-600">
            Remembered your password?{" "}
            <button
              onClick={() => router.push("/login")}
              className="font-semibold hover:underline"
              style={{ color: COLORS.primary }}
            >
              Login here
            </button>
          </p>
          {onSwitchToRegister && (
            <p className="text-gray-600 mt-2">
              Don't have an account?{" "}
              <button
                onClick={onSwitchToRegister}
                className="font-semibold hover:underline"
                style={{ color: COLORS.primary }}
              >
                Register here
              </button>
            </p>
          )}
        </div>
      </div>
    </div>
  );
}